# Shell aliases

# ------------------------------------------------------------------------------
# Navigation
# ------------------------------------------------------------------------------
alias ..="cd .."
alias ...="cd ../.."
alias ....="cd ../../.."
alias .3="cd ../../.."
alias .....="cd ../../../.."
alias .4="cd ../../../.."
alias -- -="cd -"

# ------------------------------------------------------------------------------
# Listing
# ------------------------------------------------------------------------------
alias ls="command ls --color"
alias la="ls -Gla"
alias ll="ls -l"
alias lh="ls -lh"
alias lsd='ls -l | grep "^d"'
alias tree='tree -C'

# ------------------------------------------------------------------------------
# Editors
# ------------------------------------------------------------------------------
alias vi="vim"

# ------------------------------------------------------------------------------
# Grep
# ------------------------------------------------------------------------------
alias grep='grep --color=auto'
alias egrep='egrep --color=auto'
alias fgrep='fgrep --color=auto'

# ------------------------------------------------------------------------------
# Utilities
# ------------------------------------------------------------------------------
alias wget='wget -c'
alias diff='colordiff'
alias bc='bc -l'
alias mountf="mount | column -t"
alias timestamp='date +%s'
alias datebkp='date +"%Y%m%d-%H%M%S"'

# ------------------------------------------------------------------------------
# Hex dump
# ------------------------------------------------------------------------------
type -t hd > /dev/null || alias hd="hexdump -C"

# ------------------------------------------------------------------------------
# MD5
# ------------------------------------------------------------------------------
type -t md5sum > /dev/null || alias md5sum="md5"

# ------------------------------------------------------------------------------
# Cleanup
# ------------------------------------------------------------------------------
alias cleanup-folder="find . -type f -name '*.DS_Store' -ls -delete"

# ------------------------------------------------------------------------------
# Open command (Linux)
# ------------------------------------------------------------------------------
if [[ "$OSTYPE" =~ ^linux ]]; then
    alias open="xdg-open"
fi

# ------------------------------------------------------------------------------
# Git
# ------------------------------------------------------------------------------
alias git_branch_cleanup='git branch --merged | grep -v master >/tmp/merged-branches && vi /tmp/merged-branches && xargs git branch -d </tmp/merged-branches'
alias git-squash-branch='git reset $(git merge-base master $(git branch --show-current))'

# ------------------------------------------------------------------------------
# Crontab (macOS fix)
# ------------------------------------------------------------------------------
alias crontab="VIM_CRONTAB=true crontab"

# ------------------------------------------------------------------------------
# Python
# ------------------------------------------------------------------------------
alias python='python3'

# ------------------------------------------------------------------------------
# Kubernetes
# ------------------------------------------------------------------------------
alias k='kubectl'
alias kc='kubectx'
alias kn='kubens'

# ------------------------------------------------------------------------------
# Terraform
# ------------------------------------------------------------------------------
alias tf='terraform'

# ------------------------------------------------------------------------------
# Sudo (enable aliases with sudo)
# ------------------------------------------------------------------------------
alias sudo='sudo '
